		System.out.println("Multipication is (num1 * num2 ) = " + (num1 * num2));

		// "/" division operator
		System.out.println("division is (num1 + num2 ) = " + (num1 / num2));

		// "%" Moduler operator
		System.out.println("Moduler is (num1 % num2 ) = " + num1 % num2);

