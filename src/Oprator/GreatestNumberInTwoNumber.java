package Oprator;

public class GreatestNumberInTwoNumber {

	public static void main(String[] args) {

		int a = 25;
		int b = 78;

		int res = a > b ? a : b;
		System.out.println(res + " number is greatest number");

		a = 85;
		b = 10;
		int res1 = a > b ? a : b;
		System.out.println(res1 + " number is greatest number");

	}

}
