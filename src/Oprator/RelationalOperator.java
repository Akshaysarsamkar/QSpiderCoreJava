package Oprator;

public class RelationalOperator {

	public static void main(String[] args) {
		
		
		int a = 10;
		int b = 20;
		
//      ' == '  is Equals to
		System.out.println(a == b);  // false 
		
//		' != '  not equals to 
		System.out.println(a != b); // true 
		
//		' > '   greater than
		System.out.println(a>b); //false
		
//		' < '   less than 
		System.out.println(a < b); //true 
		
		
//		' >= '  greater then or equals to
		System.out.println(a >= b);  //flase 
		
		
//		' <= '  less than or equals to 
		System.out.println(a <= b ); // true 
	}

}
