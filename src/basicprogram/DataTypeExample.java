package basicprogram;

//Write a java program to store int value into the byte, short, long, float, double, char type variable.
public class DataTypeExample {

	public static void main(String[] args) {

		byte houseNo = 10;

		short galiNo = 250;

		long pinCode = 435510;

		float salary = 95200.58f;

		double persentile = 85.20;

		char gender = 'M';

		System.out.println("House No :" + houseNo);

		System.out.println("Gali No :" + galiNo);

		System.out.println("PinCode :" + pinCode);

		System.out.println("Salary:" + salary);

		System.out.println("Persentile:" + persentile);

		System.out.println("Gender :" + gender);

	}

}
